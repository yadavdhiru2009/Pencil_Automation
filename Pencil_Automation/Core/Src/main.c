/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MPU Configuration--------------------------------------------------------*/
  MPU_Config();

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_I2C1_Init();
  /* USER CODE BEGIN 2 */
  HAL_ADC_Start(&hadc1);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  switch (currentState)
	  {
		  case MACHINE_STATUS:
			  Machine_Status();		//Diagnostic stage : test all the sensor input is down or not, check actuctor move slightly back-forth
			  	  	  	  	  	  	//If these thing happen then move to MACHINE_STATUS to HOPPER
			  break;
		  case HOPPER:				//This state is responsable to check the hopper is filled or not if filled then rotatry DC motor start
			  	  	  	  	  	  	//rotating jump to HOPPER to ROTARY_ACTUATOR else HOPPER to FAULT_TRIGGER
			  Hopper();
			  break;
		  case ROTARY_ACTUATOR:		//This state is set count to 0 for no of pencils. and move state to COUNTER_BOX
			  Rotary_Actuator();
			  break;
		  case COUNTER_BOX:			//increment the counter based on counter sensor. if count reach to FIXED_NO then ACTUACTOR trigger to drop
			  	  	  	  	  	    // pencil and change the state from COUNTER_BOX to CONVEYOR_BELT
			  Counter_Box();
			  break;
		  case CONVEYOR_BELT:	    //in this state conveyor motor is continuosly moving with the fixed steps then change the state from CONVEYOR to WEIGHING
			  Conveyor_Belt();
			  break;
		  case WEIGHING_MACHINE:	//in this state read the weighing weight approx with 2% of margin. if calculated weight is matched with real_wgt then
			  	  	  	  	  	    //move to PACKING_CONVEYOR otherwise move to WASTE_CONTAINER
			  HAL_ADC_PollForConversion(&hadc1, 100);
			  Weighing_Machine(HAL_ADC_GetValue(&hadc1));
			  break;
		  case PACKING_CONVEYOR:	//In this state, Pencils bundle pushing it the pencil box by pneumatic piston and conveyor 2 moves and drop the pencil
			  	  	  	  	  	    //bundle to final container. Move to FINAL_CONTAINER
			  Packing_Conveyor();
			  break;
		  case FINAL_CONTAINER:		//in this state, Reset all the values and set the state machine variable to HOPPER
			  Final_Container();
			  break;
		  case FAULT_TRIGGER:		//In this state, Fault triggered and send string to RPI with touch panel
			  if(Fault_Trigger() == 1)
				  HAL_I2C_Master_Transmit(&hi2c1, RPI_ADDRESS, "Fault Triggered!!", (sizeof(uint8_t)*18), 100);
			  break;
		  case WASTE_CONTAINER:
			  Waste_Container(); //if weighing weight values are different then pencil bundle drop in waste container and set the state machine variable to HOPPER
			  break;
		  default:
			  break;
	  }
  }
  /* USER CODE END 3 */
}

